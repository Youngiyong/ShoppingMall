package spring.mvc.domain;

public class ShopVO {
	
	private String i_Path;
	private String p_Name;
	private long p_Grade;
	private int p_Price;
	private String p_Color;
	
	private String p_Size;
	private String p_Cate;
	
	
	
	public String getI_Path() {
		return i_Path;
	}
	public void setI_Path(String i_Path) {
		this.i_Path = i_Path;
	}
	public String getP_Name() {
		return p_Name;
	}
	public void setP_Name(String p_Name) {
		this.p_Name = p_Name;
	}
	public long getP_Grade() {
		return p_Grade;
	}
	public void setP_Grade(long p_Grade) {
		this.p_Grade = p_Grade;
	}
	public int getP_Price() {
		return p_Price;
	}
	public void setP_Price(int p_Price) {
		this.p_Price = p_Price;
	}
	public String getP_Color() {
		return p_Color;
	}
	public void setP_Color(String p_Color) {
		this.p_Color = p_Color;
	}
	public String getP_Size() {
		return p_Size;
	}
	public void setP_Size(String p_Size) {
		this.p_Size = p_Size;
	}
	public String getP_Cate() {
		return p_Cate;
	}
	public void setP_Cate(String p_Cate) {
		this.p_Cate = p_Cate;
	}
	
	
	
	
}
