package spring.mvc.domain;

public class OrderAddrVO {

    private String pa_Code;
    private String m_Code;
    private String o_Name;
    private String o_Tel;
    private String o_Addr;
    private String o_Post;

    public String getPa_Code() { return pa_Code; }
    public void setPa_Code(String pa_Code) { this.pa_Code = pa_Code; }
    public String getM_Code() { return m_Code; }
    public void setM_Code(String m_Code) { this.m_Code = m_Code; }
    public String getO_Name() { return o_Name; }
    public void setO_Name(String o_Name) { this.o_Name = o_Name; }
    public String getO_Tel() { return o_Tel; }
    public void setO_Tel(String o_Tel) { this.o_Tel = o_Tel; }
    public String getO_Addr() { return o_Addr; }
    public void setO_Addr(String o_Addr) { this.o_Addr = o_Addr; }
    public String getO_Post() { return o_Post; }
    public void setO_Post(String o_Post) { this.o_Post = o_Post; }
}
