package spring.mvc.domain.vo;

public class MemberVO {

    private String m_Code;
    private String m_Id;
    private String m_Name;
    private String m_Pass;
    private String m_Tel;
    private String m_Email;
    private String m_Addr;
    private int m_Age;
    private String m_Country;

    public String getM_Code() { return m_Code; }
    public void setM_Code(String m_Code) { this.m_Code = m_Code; }
    public String getM_Id() { return m_Id; }
    public void setM_Id(String m_Id) { this.m_Id = m_Id; }
    public String getM_Name() { return m_Name; }
    public void setM_Name(String m_Name) { this.m_Name = m_Name; }
    public String getM_Pass() { return m_Pass; }
    public void setM_Pass(String m_Pass) { this.m_Pass = m_Pass; }
    public String getM_Tel() { return m_Tel; }
    public void setM_Tel(String m_Tel) { this.m_Tel = m_Tel; }
    public String getM_Email() { return m_Email; }
    public void setM_Email(String m_Email) { this.m_Email = m_Email; }
    public String getM_Addr() { return m_Addr; }
    public void setM_Addr(String m_Addr) { this.m_Addr = m_Addr; }
    public int getM_Age() { return m_Age; }
    public void setM_Age(int m_Age) { this.m_Age = m_Age; }
    public String getM_Country() { return m_Country; }
    public void setM_Country(String m_Country) { this.m_Country = m_Country; }
}
